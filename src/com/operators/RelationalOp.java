package com.operators;
//program for relational operators
public class RelationalOp {
public static void main(String[] args)
{
    int a=5;
    int b=7;
    System.out.println(a<b);
    System.out.println(a<=b);
    System.out.println(a>b);
    System.out.println(a>=b);
    System.out.println(a==b);
}
}
