package com.Loops;
/*
WRITE A PROGRAM TO PRINT BRIGHT IT CAREER 10 TIMES USING FOR LOOP
*/
public class BrightFuture {
    public static void main(String[] args)
    {
        String a="Bright IT Career";
        for (int i=0;i<10;i++)
        {
            System.out.println((i+1) + ". " + a);
        }
    }
}
